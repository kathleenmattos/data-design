		<h1>Use Case</h1>
		<p><strong>Title:</strong> Brian posting to reddit</p>
		<p><strong>Description:</strong> Brian wants to share a game news story with people with similar interests.</p>
		<p><strong>Name of User and Their Role:</strong> Brian; an avid news reader who enjoys sharing and discussing his interests</p>
		<p><strong>Usage Preconditions:</strong> Must have a Reddit account and be logged in. Also be subscribed to the gaming subreddits.  </p>
		<p><strong>Usage Postconditions:</strong> Article is posted and Brian can discuss the news in the comments. </p>
		<ul>
			<li>Brian enters www.reddit.com into his web browser.</li>
			<li>Server returns the Reddit homepage.</li>
			<li>Then Brian clicks on the "New Post" button</li>
			<li>The server returns a new pop up window asking for the content of the new post</li>
			<li>Brian clicks on the dropdown menu and chooses the gaming subreddit called r/gaming</li>
			<li>Brian fills out the information fields for "Title" and "Description" and pastes a link to the news story.</li>
			<li>He then clicks the "Post" button </li>
			<li>He wants to check that the post is up so he clicks the subscription button and selects r/gaming from the pop up menu.</li>
			<li>The server returns the gaming subreddit.</li>
			<li>Brian clicks the "sort" drop down menu and choses "new".</li>
			<li>The server returns the newest posts.</li>
			<li>Brian is now able to see his post at the top of the page.</li>
		</ul>
		<p><strong>Frequency of Use:</strong> Daily</p>
